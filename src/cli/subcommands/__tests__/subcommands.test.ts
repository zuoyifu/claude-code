import { describe, expect, test } from 'bun:test'
import { Command } from '@commander-js/extra-typings'

describe('C5 subcommands', () => {
  test('registerAllSubcommands 注册全部 subcommand', async () => {
    const { registerAllSubcommands } = await import('../index.js')
    const program = new Command()
    registerAllSubcommands(program)
    const cmds = program.commands.map(c => c.name())
    expect(cmds).toContain('mcp')
    expect(cmds).toContain('auth')
    expect(cmds).toContain('plugin')
    expect(cmds).toContain('doctor')
    expect(cmds).toContain('update')
    expect(cmds).toContain('agents')
    expect(cmds).toContain('autonomy')
  })

  test('mcp define 注册 serve/remove/list/get', async () => {
    const { define: defineMcp } = await import('../mcp.js')
    const program = new Command()
    defineMcp(program)
    const mcp = program.commands.find(c => c.name() === 'mcp')
    expect(mcp).toBeDefined()
    const subCmds = mcp!.commands.map(c => c.name())
    expect(subCmds).toContain('serve')
    expect(subCmds).toContain('remove')
    expect(subCmds).toContain('list')
    expect(subCmds).toContain('get')
  })

  test('auth define 注册 login/logout/status', async () => {
    const { define: defineAuth } = await import('../auth.js')
    const program = new Command()
    defineAuth(program)
    const auth = program.commands.find(c => c.name() === 'auth')
    expect(auth).toBeDefined()
    const subCmds = auth!.commands.map(c => c.name())
    expect(subCmds).toContain('login')
    expect(subCmds).toContain('logout')
    expect(subCmds).toContain('status')
  })

  test('plugin define 注册 validate/list/install/uninstall/marketplace', async () => {
    const { define: definePlugin } = await import('../plugin.js')
    const program = new Command()
    definePlugin(program)
    const plugin = program.commands.find(c => c.name() === 'plugin')
    expect(plugin).toBeDefined()
    const subCmds = plugin!.commands.map(c => c.name())
    expect(subCmds).toContain('validate')
    expect(subCmds).toContain('list')
    expect(subCmds).toContain('install')
    expect(subCmds).toContain('uninstall')
    expect(subCmds).toContain('marketplace')
  })

  test('autonomy define 注册 status/runs/flows/flow', async () => {
    const { define: defineAutonomy } = await import('../autonomy.js')
    const program = new Command()
    defineAutonomy(program)
    const autonomy = program.commands.find(c => c.name() === 'autonomy')
    expect(autonomy).toBeDefined()
    const subCmds = autonomy!.commands.map(c => c.name())
    expect(subCmds).toContain('status')
    expect(subCmds).toContain('runs')
    expect(subCmds).toContain('flows')
    expect(subCmds).toContain('flow')
  })

  test('每个 define 是函数（从 index.ts re-export）', async () => {
    const mod = await import('../index.js')
    expect(typeof mod.defineMcp).toBe('function')
    expect(typeof mod.defineAuth).toBe('function')
    expect(typeof mod.definePlugin).toBe('function')
    expect(typeof mod.defineAgents).toBe('function')
    expect(typeof mod.defineDoctor).toBe('function')
    expect(typeof mod.defineUpdate).toBe('function')
    expect(typeof mod.defineServer).toBe('function')
    expect(typeof mod.defineAutoMode).toBe('function')
    expect(typeof mod.defineAutonomy).toBe('function')
    expect(typeof mod.defineTask).toBe('function')
  })
})
