import { globSync, readFileSync } from 'node:fs'
import path from 'node:path'
import type { CommandCategory } from './types.js'

const COMMAND_GLOB = 'commands/*/*/index.ts'

/**
 * 扫描结果——单个命令的路径元数据。
 */
export interface ScannedCommand {
  /** 主题分组（从路径推导）。 */
  category: string
  /** 命令名（从路径推导）。 */
  name: string
  /** 相对路径，例如 'commands/session/clear/index.ts'。 */
  relativePath: string
}

const VALID_CATEGORIES: ReadonlySet<string> = new Set<CommandCategory>([
  'session-info',
  'session',
  'mcp',
  'model',
  'config',
  'memory',
  'skills',
  'plugins',
  'tasks',
  'ui',
  'debug',
  'review',
  'version',
  'files',
  'bridge',
  'daemon',
  '_misc',
])

/**
 * 扫描 srcRoot/commands/<category>/<name>/index.ts。
 * 同时被 dev mode 和 build.ts 调用。
 *
 * 校验：
 * 1. 路径必须匹配 commands/<category>/<name>/index.ts
 * 2. <category> 必须是 CommandCategory 联合类型之一
 *
 * @throws 如果路径格式不符或 category 非法
 */
export function scanCommands(srcRoot: string): ScannedCommand[] {
  const files = globSync(COMMAND_GLOB, { cwd: srcRoot })
  const result: ScannedCommand[] = []
  for (const file of files) {
    // commands/<category>/<name>/index.ts
    const parts = file.split('/')
    if (parts.length !== 4 || parts[3] !== 'index.ts') {
      throw new Error(
        `Unexpected command path: ${file} (expected commands/<category>/<name>/index.ts)`,
      )
    }
    const [, category, name] = parts
    if (!VALID_CATEGORIES.has(category)) {
      throw new Error(
        `Unknown command category: '${category}' in ${file}. Add to CommandCategory union in types.ts or rename the directory.`,
      )
    }
    // C3+C8 Plan B: skip commands without a static object default export.
    // Several commands (reset-limits, extra-usage, context) use named exports
    // only, and some (login) use `export default () => ...` (a factory
    // function). The generated.ts spread pattern `{ ...cmd }` requires a
    // static object. Until those commands are refactored, they stay in the
    // central commands.ts array and are excluded from REGISTERED_COMMANDS.
    const fullPath = path.resolve(srcRoot, file)
    const content = readFileSync(fullPath, 'utf8')
    // Must have `export default` AND it must not be an arrow function.
    if (!/export\s+default\b/.test(content)) {
      continue
    }
    if (
      /export\s+default\s*\(/.test(content) ||
      /export\s+default\s*\w+\s*=>/.test(content)
    ) {
      continue
    }
    result.push({
      category,
      name,
      relativePath: file.replace(/\.ts$/, '.js'),
    })
  }
  return result
}

/**
 * 把扫描结果中的特殊字符替换为合法 JS 标识符。
 */
function sanitizeIdentifier(s: string): string {
  return s.replace(/[^a-zA-Z0-9]/g, '_')
}

/**
 * 生成 generated.ts 内容。
 * 输出格式：纯静态 import + 数组，tsc 可类型检查，Bun.build 可正确打包。
 *
 * @param commands 扫描结果
 * @returns generated.ts 文件内容字符串
 */
export function generateRegistryCode(commands: ScannedCommand[]): string {
  const imports = commands
    .map(c => {
      const ident = `cmd_${sanitizeIdentifier(c.category)}_${sanitizeIdentifier(c.name)}`
      // generated.ts 位于 src/commands/_registry/generated.ts
      // 相对 import 路径：../../<relativePath>
      return `import ${ident} from '../../${c.relativePath}'`
    })
    .join('\n')

  const entries = commands
    .map(c => {
      const ident = `cmd_${sanitizeIdentifier(c.category)}_${sanitizeIdentifier(c.name)}`
      return `  _reg(${ident}, '${c.category}', '${c.relativePath}')`
    })
    .join(',\n')

  const importBlock = imports ? `${imports}\n\n` : ''
  // 空数组输出 `[]`，非空数组每行一个 entry
  const arrayBody = entries ? `\n${entries}\n` : ''

  return `// @ts-nocheck — AUTO-GENERATED by scanner.ts, DO NOT EDIT
// Regenerated on every 'bun run dev' and 'bun run build'.
import type { RegisteredCommand } from './types.js'

/** Attach category+sourcePath to a command object WITHOUT triggering property getters. */
function _reg(cmd, category, sourcePath) {
  return Object.assign(Object.create(cmd), { category, sourcePath })
}

${importBlock}export const REGISTERED_COMMANDS: RegisteredCommand[] = [${arrayBody}]
`
}
